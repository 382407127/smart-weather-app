     * 根据天气代码获取天气主题
     */
    fun getWeatherTheme(iconCode: String): WeatherTheme {
        return when (iconCode.toIntOrNull() ?: 100) {
            // 晴
            100, 150 -> WeatherTheme.SUNNY
            // 多云
            101, 102, 103, 151, 152, 153 -> WeatherTheme.CLOUDY
